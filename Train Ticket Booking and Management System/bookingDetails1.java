                   + " FROM PPASSENGER P,PTICKET TI,PTRAIN T WHERE P.p_id = TI.PT_ID and T.T_ID = TI.TT_ID"
                   + " and p_id ='"+Integer.parseInt(data)+"' and T_ID = '"+Integer.parseInt(data1)+"'";
