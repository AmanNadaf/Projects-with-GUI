              String tdate = rs.getString("TIC_DATE");
              int no_of_tickets = rs.getInt("TIC_NOS");
              String tname = rs.getString("TNAME");
              String arrival = rs.getString("ARRIVAL");
              String destination = rs.getString("DESTINATION");
              String dptime = rs.getString("DP_TIME");
              String artime = rs.getString("AR_TIME");
              int fare = rs.getInt("FARE");
              int tfare = data2 * fare;
              
              jl1.setText(pname);
              jl2.setText(pPhNo);
              jl3.setText(tdate);
              jl4.setText(String.valueOf(no_of_tickets));
